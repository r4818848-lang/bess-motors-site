import crypto from "crypto";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const stockPath = path.join(__dirname, "../src/lib/vehicle-stock-photos.ts");
const text = fs.readFileSync(stockPath, "utf8");

const files = new Set();
for (const m of text.matchAll(/file:\s*"([^"]+)"/g)) {
  files.add(m[1]);
}

function hashPath(filename) {
  const md5 = crypto.createHash("md5").update(filename).digest("hex");
  return `${md5[0]}/${md5.slice(0, 2)}`;
}

function thumbUrl(filename, width = 1280) {
  const p = hashPath(filename);
  return `https://upload.wikimedia.org/wikipedia/commons/thumb/${p}/${filename}/${width}px-${filename}`;
}

const hashEntries = [...files]
  .sort()
  .map((f) => `  ${JSON.stringify(f)}: ${JSON.stringify(hashPath(f))},`)
  .join("\n");

const outPath = path.join(__dirname, "../src/lib/wikimedia-thumb.ts");
const out = `/** @generated by scripts/gen-wikimedia-urls.mjs — do not edit hashes by hand */
const FILE_HASH: Record<string, string> = {
${hashEntries}
};

export function wikimediaThumbUrl(filename: string, width = 1280): string {
  const hash = FILE_HASH[filename];
  if (!hash) return "";
  return \`https://upload.wikimedia.org/wikipedia/commons/thumb/\${hash}/\${filename}/\${width}px-\${filename}\`;
}
`;

fs.writeFileSync(outPath, out);
console.log(`Wrote ${files.size} entries to ${outPath}`);

// Print key models for verification
for (const f of [
  "Mercedes_s-class_w223_black_(1).jpg",
  "MERCEDES_MAYBACH_S-CLASS_(W222)_China_(9).jpg",
  "Rolls-Royce_Wraith,_Geneva_Motor_Show_2013_(8711475097).jpg",
]) {
  console.log(thumbUrl(f));
}
